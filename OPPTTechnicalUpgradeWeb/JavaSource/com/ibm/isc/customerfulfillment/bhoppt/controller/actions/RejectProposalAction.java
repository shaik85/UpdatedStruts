	protected OPPTViewBean executeAction(ActionForm form, OPPTSession session) throws Exception {
		OPPTViewBean viewBean = null;

		ProposalCustomDTO currentProposal = getCurrentProposal(session);
		if(null != currentProposal){
			RejectProposalCustomDTO customDTO = new RejectProposalCustomDTO();
			setTransactionInformation(customDTO, session);
			customDTO.setProposalDTO(currentProposal);
			customDTO.setConfirmationRequired(true);
			
			try{
				getProposalManager().rejectRequest(customDTO);
				addDataStoreEntry(session, currentProposal);
				setProposalToBeRejected(session, customDTO);
				viewBean = new RejectionReasonViewBean();
				setForwardName(getRole(session));
			}
			catch(ServiceException se){
				viewBean = handleErrorReport(session, se.getErrorReport());
			}
		}
		else{
			viewBean = new ErrorViewBean(getErrorReport(ErrorCodeConstants.PA00));
			setForwardName(ForwardNameConstants.PAGE_ERROR);
		}
		
		return viewBean;
	}

	/** 
	 * Overridden method - Enter your description here. 
	 * 
	 * <br/><PRE> 
	 * date Nov 14, 2003 
	 * 
	 * revision date author reason 
	 * 
	 * </PRE><br/> 
	 * 
	 * @see com.ibm.support.oppt.OPPTAction#getActionName() 
	 * @return 
	 * 
	 */
	protected String getActionName() {
		return ActionNameConstants.REJECT_PROPOSAL;
	}

	private void setProposalToBeRejected(OPPTSession session, RejectProposalCustomDTO rejectProposal){
		if(null != session && null != rejectProposal){
			session.storeObjectToSession(SessionConstants.PROPOSAL_TO_BE_REJECTED, rejectProposal);
		}
	}

	private ProposalCustomDTO getCurrentProposal(OPPTSession session) {
		ProposalCustomDTO currentProposal = null;
		if(null != session){
			currentProposal = (ProposalCustomDTO)session.getObjectFromSession(SessionConstants.CURRENT_PROPOSAL);
		}
		return currentProposal;
	}

	private void setForwardName(int role){
		switch(role){
			case Constants.PROPOSAL_CREATE_APPROVER: 
								setForwardName(ForwardNameConstants.PAGE_CREATE_APPROVE_PROPOSALS_FOR_APPROVAL_REJECTION_REASON);
								break;

			case Constants.PROPOSAL_APPROVER:
			default:
								setForwardName(ForwardNameConstants.PAGE_PROPOSAL_FOR_APPROVAL_REJECTION_REASON);
								break;
		}
	}

