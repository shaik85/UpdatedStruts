	private CreateGroupCustomDTO getCustomDto(OPPTSession session){
		CreateGroupCustomDTO createCustomDto = new CreateGroupCustomDTO();
		if(null != session){
			GroupsCustomDTO customDto = (GroupsCustomDTO)session.getObjectFromSession(SessionConstants.SESSION_GROUP_LIST_DETAIL);
			createCustomDto.setSalesAreas(customDto.getSalesAreas());
			createCustomDto.setSelectedSalesArea(customDto.getSelectedSalesArea());
		}
		
		return createCustomDto;
	}

