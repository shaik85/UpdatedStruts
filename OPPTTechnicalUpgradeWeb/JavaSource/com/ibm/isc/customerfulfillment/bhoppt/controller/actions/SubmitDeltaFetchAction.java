	protected OPPTViewBean executeAction(ActionForm form, OPPTSession session)
		throws Exception {
		
		OPPTViewBean viewBean=null;
		ProposalCustomDTO customDto;
		DeltaFetchActionForm actionForm = (DeltaFetchActionForm)form;
		customDto = getCurrentProposal(session);
		
		DeltaFetchCustomDTO deltaCustomDto = new DeltaFetchCustomDTO();
		setTransactionInformation(deltaCustomDto, session);
		
		deltaCustomDto.setProposalDTO(customDto);
		setTransactionInformation(deltaCustomDto, session);
		if(Constants.SKIP_IDENTIFIER.equalsIgnoreCase(actionForm.getDeltaFetchType())){
			deltaCustomDto.setSkip(true);
		}
		else{
			deltaCustomDto.setSkip(false);
		}

		try {
			deltaCustomDto = getProposalManager().deltaFetch(deltaCustomDto);
			ErrorReport errorReport = deltaCustomDto.getMessageReport();

			if(null == errorReport){
				setForwardName(ForwardNameConstants.ACTION_PROPOSAL_CONTRACT_DETAILS);
			}
			else{
				viewBean = new MessageViewBean();
				viewBean.setErrorReport(errorReport);
				setForwardName(ForwardNameConstants.PAGE_MESSAGE);
			}
		}
		catch(ServiceException ex){
			viewBean = new ErrorViewBean();
			viewBean.setErrorReport(ex.getErrorReport());
			setForwardName(ForwardNameConstants.PAGE_ERROR);

		}
		return viewBean;	
	}

