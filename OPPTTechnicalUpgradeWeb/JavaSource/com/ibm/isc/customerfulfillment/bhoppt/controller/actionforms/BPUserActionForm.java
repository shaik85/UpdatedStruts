	public ErrorReport validate() {
		
		ErrorReport errorReport = new ErrorReport();
		boolean condition = true;
		if(!"edit".equalsIgnoreCase(getSource()) && isFieldEmpty(getUserId())){
			errorReport.put(new ErrorKey(ErrorCodeConstants.PAF44, OPPTMessageCatalog.TYPE_PRESENTATION),null);
		}

		if(isSelectEmpty(getRole())){
			errorReport.put(new ErrorKey(ErrorCodeConstants.PAF45, OPPTMessageCatalog.TYPE_PRESENTATION),null);
		}

		if(isSelectEmpty(getPrimarySalesOrganization())){
			errorReport.put(new ErrorKey(ErrorCodeConstants.PAF47, OPPTMessageCatalog.TYPE_PRESENTATION),null);
		}

		if(isSelectEmpty(getManager())){
			errorReport.put(new ErrorKey(ErrorCodeConstants.PAF50, OPPTMessageCatalog.TYPE_PRESENTATION),null);
		}
		if("C".equalsIgnoreCase(getAccessRequired()) && isArrayEmpty(getListNumbers())){
			errorReport.put(new ErrorKey(ErrorCodeConstants.PAF55, OPPTMessageCatalog.TYPE_PRESENTATION),null);
		}
		if("L".equalsIgnoreCase(getAccessRequired()) && isArrayEmpty(getLegacyNumber())){
			errorReport.put(new ErrorKey(ErrorCodeConstants.PAF56, OPPTMessageCatalog.TYPE_PRESENTATION),null);
		}
		if("L".equalsIgnoreCase(getAccessRequired()) && checkLegacySequency()){
			errorReport.put(new ErrorKey(ErrorCodeConstants.PAF57, OPPTMessageCatalog.TYPE_PRESENTATION),null);
		}
	
		return errorReport;
	}
	
	/**
	 * @param value
	 * @return
	 */
	private boolean isArrayEmpty(String[] value){
		boolean condition = true;
		if(null != value){
			int length = value.length;
			for(int i=0; i < length; i++){
				if(!"".equals(value[i])){
					condition = false;
				}
			}
		}
		return condition;
	}
	
	/**
	 * @return
	 */
	private boolean checkLegacySequency(){
		boolean condition=false;
		
		if(null != getLegacyNumber()){
			int arrayLength = getLegacyNumber().length;
	
			for(int i=0; i < arrayLength; i++){
				if((!isFieldEmpty(getLegacyNumber()[i]) && isFieldEmpty(getSequenceNumber()[i]))
					|| (isFieldEmpty(getLegacyNumber()[i]) && !isFieldEmpty(getSequenceNumber()[i]))){
	
						condition=true;
				}
			}
		}
	
		return condition;
	}


