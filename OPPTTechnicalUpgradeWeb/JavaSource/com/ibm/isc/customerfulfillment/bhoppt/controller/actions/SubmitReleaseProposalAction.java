public class SubmitReleaseProposalAction extends OPPTLongAction 
{
	/** 
	 * Overridden method - Enter your description here. 
	 * 
	 * <br/><PRE> 
	 * date Nov 14, 2003 
	 * 
	 * revision date author reason 
	 * 
	 * </PRE><br/> 
	 * 
	 * @param form
	 * @param session
	 * @return
	 * @throws Exception 
	 * 
	 */
	protected OPPTViewBean executeAction(ActionForm form, OPPTSession session)
		throws Exception {
		
		OPPTViewBean viewBean=null;
		ErrorReport errorReport=null;
		ReleaseProposalActionForm actionForm;
		ReleaseLockOnProposalCustomDTO customDto;

		actionForm = (ReleaseProposalActionForm)form; 
		customDto = new ReleaseLockOnProposalCustomDTO();
		
		setTransactionInformation(customDto, session);
		customDto.setSessionId(session.getSessionId());
		customDto.setProposalName(actionForm.getProposalName());
	

		try {
			customDto = getProposalManager().releaseLockOnProposal(customDto);
			errorReport = customDto.getMessageReport();
			if(null == errorReport){
				setForwardName(ForwardNameConstants.ACTION_ADMINISTRATOR);
			}
			else{
				viewBean = new MessageViewBean();
				viewBean.setErrorReport(errorReport);
				setForwardName(ForwardNameConstants.PAGE_MESSAGE);
			}

		}
		catch(ServiceException ex){
			viewBean = handleServiceException(session, ex.getErrorReport());
		}
	
		return viewBean;
	}

	/** 
	 * Overridden method - Enter your description here. 
	 * 
	 * <br/><PRE> 
	 * date Nov 14, 2003 
	 * 
	 * revision date author reason 
	 * 
	 * </PRE><br/> 
	 * 
	 * @see com.ibm.support.oppt.OPPTAction#getActionName() 
	 * @return 
	 * 
	 */
	protected String getActionName() {
		return ActionNameConstants.SUBMIT_RELEASE_PROPOSAL;
	}
	
	/** 
	 * Overridden method - Usage of back button is not allowed for this action. So return false. 
	 * 
	 * <br/><PRE> 
	 * date Feb 10, 2004 
	 * 
	 * revision date author reason 
	 * 
	 * </PRE><br/> 
	 * 
	 * @see com.ibm.isc.customerfulfillment.bhoppt.controller.OPPTAction#isBackButtonAllowed() 
	 * @return 
	 * 
	 */
	protected boolean isBackButtonAllowed() {
		return false;
	}

	/**
     * @author thirumalai
     */
    protected boolean validateCurrentViewContent(OPPTSession session) {
		boolean valid = false;
		
		OPPTViewBean viewBean = session.getCurrentViewBean();
		String currentForward = session.getCurrentForward();
		String actualForward = ForwardNameConstants.PAGE_RELEASE_PROPOSAL;

		if(null != viewBean && null != currentForward){
			if(viewBean instanceof ReleaseProposalViewBean && currentForward.equals(actualForward)){
				valid = true;
			}
		}

		if(!valid){
			session.setCurrentViewBean(new ReleaseProposalViewBean());
			session.setCurrentForward(actualForward);
			valid = true;
		}
		
		return valid;
	}
}
