	protected OPPTViewBean executeAction(ActionForm form, OPPTSession session)
		throws Exception {
		
		OPPTViewBean viewBean=null;
		VarianceHeaderInformationCustomDTO varianceCustomDto = getFromSession(session);
		setTransactionInformation(varianceCustomDto, session);
		if(null != varianceCustomDto){
			varianceCustomDto = getProposalManager().deleteTempVarianceValues(varianceCustomDto);
			ErrorReport errorReport = varianceCustomDto.getMessageReport();
		
			if(null == errorReport){
				setForwardName(ForwardNameConstants.ACTION_CONTRACT_MAINTENANCE);
			}
			else{
				viewBean = new MessageViewBean();
				viewBean.setErrorReport(errorReport);
				setForwardName(ForwardNameConstants.PAGE_MESSAGE);
			}
		
		}
		
		return viewBean;	
	}

	/** 
	 * Overridden method - Enter your description here. 
	 * 
	 * <br/><PRE> 
	 * date Nov 14, 2003 
	 * 
	 * revision date author reason 
	 * 
	 * </PRE><br/> 
	 * 
	 * @see com.ibm.support.oppt.OPPTAction#getActionName() 
	 * @return 
	 * 
	 */
	protected String getActionName() {
		return ActionNameConstants.DELETE_TEMP_VARIANCE;
	}
	
	private VarianceHeaderInformationCustomDTO getFromSession(OPPTSession session){
		VarianceHeaderInformationCustomDTO customDto = null;
		if(null != session){
			customDto = (VarianceHeaderInformationCustomDTO)session.getObjectFromSession(SessionConstants.VARIANCE_CHECK);
		}
		
		return customDto;
	}

