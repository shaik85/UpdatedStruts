	/* (non-Javadoc)
	 * @see com.ibm.isc.customerfulfillment.bhoppt.controller.OPPTActionForm#validate()
	 */
	public ErrorReport validate() {
		ErrorReport errorReport = new ErrorReport();
		String discountCheck = validateDiscountPercent(getDiscount());
		
		if(isSelectEmpty(getSwoList())){
			errorReport.put(new ErrorKey(ErrorCodeConstants.PAF78, OPPTMessageCatalog.TYPE_PRESENTATION),null);
		}
		
		if(null != discountCheck){
			errorReport.put(new ErrorKey(discountCheck, OPPTMessageCatalog.TYPE_PRESENTATION),null);
		}

		return errorReport;
	}
	
	/**
	 * @param discountValue
	 * @return
	 */
	private String validateDiscountPercent(String discountValue){
		String result = null;
		try{
			double discount = Double.parseDouble(discountValue);
			if(100 <= discount){
				result = ErrorCodeConstants.PAF80;
			}
			
			if(-1 != discountValue.indexOf(".")){
				int afterDecimal = discountValue.length() - discountValue.indexOf(".");
				if(3 < afterDecimal){
					result = ErrorCodeConstants.PAF81;
				}
			}
		}
		catch(NumberFormatException ex){
			result = ErrorCodeConstants.PAF79;
		}
		return result; 
	}
	
