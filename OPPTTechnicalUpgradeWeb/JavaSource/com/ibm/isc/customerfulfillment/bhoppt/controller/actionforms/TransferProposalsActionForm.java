	/* (non-Javadoc)
	 * @see com.ibm.isc.customerfulfillment.bhoppt.controller.OPPTActionForm#validate()
	 */
	public ErrorReport validate() {
		
		ErrorReport errorReport = new ErrorReport();
		
		if(Integer.parseInt(sourceUserRole) != RoleConstants.CREATE_APPROVE){
			if(null == getSelectedUserId()){
				errorReport.put(new ErrorKey(ErrorCodeConstants.PAF44, OPPTMessageCatalog.TYPE_PRESENTATION),null);
			}
		}
		else{
			if(null == getCreateApproverId() && null == getApproverId() && null == getGeneralUserId()){
				errorReport.put(new ErrorKey(ErrorCodeConstants.PAF44, OPPTMessageCatalog.TYPE_PRESENTATION),null);
			}
			else if(null != getCreateApproverId() && (null != getApproverId() || null != getGeneralUserId())){
				errorReport.put(new ErrorKey(ErrorCodeConstants.PAF95, OPPTMessageCatalog.TYPE_PRESENTATION),null);
			}
			else if((null == getApproverId() && null != getGeneralUserId()) || (null != getApproverId() && null == getGeneralUserId())){
				errorReport.put(new ErrorKey(ErrorCodeConstants.PAF96, OPPTMessageCatalog.TYPE_PRESENTATION),null);
			}
		}
		
		return errorReport;
	}

