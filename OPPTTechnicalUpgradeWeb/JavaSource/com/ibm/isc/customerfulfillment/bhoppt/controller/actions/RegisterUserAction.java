	protected String getActionName() {
		return ActionNameConstants.REGISTER_USER;
	}
	
	private void storeInSession(CreateUserCustomDTO customDto, OPPTSession session){
		session.storeObjectToSession(SessionConstants.SESSION_REGISTER_USER_DETAIL,customDto);
	}

