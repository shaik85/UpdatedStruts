import com.ibm.isc.customerfulfillment.bhoppt.connector.ConnectorDTOInterface;
import com.ibm.isc.customerfulfillment.bhoppt.connector.util.ConnectorConstants;
import com.ibm.isc.customerfulfillment.bhoppt.connector.util.ConnectorUtility;

public class RetrieveInvenConnectorDTOSerializer implements InputConnectorDTOSerializerInterface
{
	/** 
	 * Constructor
	 *  
	 * <br/><PRE> 
	 * date Nov 20, 2003 
	 * 
	 * revision date author reason 
	 * 
	 * </PRE><br/> 
	 *  
	 */
	public RetrieveInvenConnectorDTOSerializer()
	{
	}

	/**
	 * This method is used to parse the given object and build object
	 * a String out of it.
	 *   
	 * <br><b>Known Bugs</b><br> 
	 * 
	 * <br><PRE> 
	 * date Nov 20, 2003 
	 * 
	 * revision date author reason 
	 * 
	 * </PRE><br> 
	 * @param connectorDTO
	 * @return String
	 * @throws Exception
	 */
	public String createNewMsg(ConnectorDTOInterface connectorDTO) throws Exception
	{
		RetrieveInvenConnectorDTO invenDTO = (RetrieveInvenConnectorDTO) connectorDTO;

		StringBuffer buffer = new StringBuffer();

		ConnectorUtility.buildMessage(invenDTO.getProposalType(), buffer);
		ConnectorUtility.buildMessage(invenDTO.getDocumentCategory(), buffer);
		ConnectorUtility.buildMessage(invenDTO.getCurrency(), buffer);
		
		ConnectorUtility.buildMessage(
			ConnectorUtility.getDateByFormat(invenDTO.getContractStartDate(), ConnectorConstants.DATE_FORMAT),
			buffer);
		
		ConnectorUtility.buildMessage(
			ConnectorUtility.getDateByFormat(invenDTO.getContractEndDate(), ConnectorConstants.DATE_FORMAT),
			buffer);

		return buffer.toString();
	}
}
